/**
 * Translate general text key.
 */
export function t(key, lang = 'vi') {
  const l = lang === 'en' ? 'en' : 'vi';
  return I18N[l][key] || I18N.en[key] || key;
}

/**
 * Bidirectional category name translation.
 */
export function tCat(catName, lang = 'vi') {
  if (!catName) return '';
  const cleanName = catName.trim();
  const l = lang === 'en' ? 'en' : 'vi';

  // Check if it is a default key (e.g. 'Food & Dining')
  if (CATEGORY_MAP[cleanName]) {
    return CATEGORY_MAP[cleanName][l];
  }

  // Check if it is a value in the map (e.g. 'Ăn uống')
  for (const [key, mapping] of Object.entries(CATEGORY_MAP)) {
    if (mapping.en === cleanName || mapping.vi === cleanName) {
      return mapping[l];
    }
  }

  // Fallback to key itself (custom categories like 'Tiện ích' or anything else)
  return cleanName;
}

/**
 * Get category icon.
 */
export function getCatIcon(catName) {
  if (!catName) return '📦';
  const cleanName = catName.trim();

  if (CATEGORY_MAP[cleanName]) {
    return CATEGORY_MAP[cleanName].icon;
  }

  for (const mapping of Object.values(CATEGORY_MAP)) {
    if (mapping.en === cleanName || mapping.vi === cleanName) {
      return mapping.icon;
    }
  }

  return '📦';
}
